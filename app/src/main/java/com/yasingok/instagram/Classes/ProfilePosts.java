package com.yasingok.instagram.Classes;

public class ProfilePosts {

    public String FotoUrl;
    public String Username;
    public String PostTitle;
    public String UserMail;

    public ProfilePosts(String fotoUrl, String username, String postTitle, String userMail) {
        FotoUrl = fotoUrl;
        Username = username;
        PostTitle = postTitle;
        UserMail = userMail;
    }
}